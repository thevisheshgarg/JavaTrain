package com.edu.pojo;

public class PojoExample {
	private String name;
	private int salary;
	
	public PojoExample() {
		
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	
}
